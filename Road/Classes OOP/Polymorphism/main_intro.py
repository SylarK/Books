#   Polymorphism
#   É a capacidade em se utilizar algo de diferentes formas. Por exemplo, uma função que tem determinadas caracteristicas em
#   uma classe, poderá ter outras características quando herdade por outra classe.
#   It refers to the use of a single type entity (method, operator or object) to represent different types in different scenarios.

print(len('Congratulations'))
print(len(['Você', 'é', 'o', 'vencedor']))
print(len({'msg':'Pegue o seu prêmio', 'money':'$ 100'}))

#   A mesma função nativa (len) operou de três formas diferentes nos exemplos citados. 
#   1   -   Tamanho da string
#   2   -   Número de itens no array
#   3   -   Número de keys no dict