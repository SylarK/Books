#   FOR
#   for statement iterates over the items of any sequence, in the order that they appear in the sequence.

setores = ['acerto', 'financeiro', 'cpd', 'vendas', 'rh', 'recepção', 'cadastradores']

for element in setores:
    print(element)

values = 5,6,8,9,7,8,5,6,1

for num in values:
    print(num*(num-1))

